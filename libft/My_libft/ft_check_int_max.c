#include "libft.h"

int		ft_check_int_max(char *str)
{
	if (str[0] == '-')
	{
		str++;
		if ((ft_strcmp(str, "2147483647") > 0))
		{
			return (-1);
		}
	}
	if ((ft_strcmp(str, "2147483647")) > 0)
	{
		return (1);
	}
	return (0);
}
